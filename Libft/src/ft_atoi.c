#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	long	sign;
	long	result;

	sign = 1;
	result = 0;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = sign * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i] - '0';
		if (result < 0 && sign == -1)
			return ((int)LONG_MIN);
		else if (result < 0 && sign == 1)
			return ((int)LONG_MAX);
		i++;
	}
	return ((int)(result * sign));
}
